const a = 7;
let b = 3;

// Atribuição aditiva
b += a; // b = b + a
console.log(b);

// Atribuição subtrativa
b -= 4;  // b = b - 4
console.log(b);

// Atribuição multiplicativa
b *= 2; // b = b * 2
console.log(b); 

// Atribuição divisao
b /= 2; // b = b / 3
console.log(b); 

// Atribuição modular
b %= 2; // b = b % 2
console.log(b); 